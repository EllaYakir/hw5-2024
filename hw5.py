from typing import Union, Tuple
import pathlib
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[pathlib.Path, str]):

        self.data_fname = pathlib.Path(data_fname)
        if not self.data_fname.exists():
            raise ValueError('Data path does not exist')
        if not isinstance(self.data_fname, pathlib.Path):
            raise TypeError('Data path must be a path to a file (either Path or str)')
        try:
            self.data = self.read_data()
        except:
            self.data = None

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        import json
        import pandas as pd
        with open(self.data_fname, 'r') as f:
            data = json.load(f)
        return pd.DataFrame(data)


    def show_age_distrib(self) -> Tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.

    Returns
    -------
    hist : np.ndarray
      Number of people in a given bin
    bins : np.ndarray
      Bin edges
        """
        # creating the histogram
        ages = self.data['age']
        ages = ages.apply(lambda x: np.nan if isinstance(x, str) else x)
        ages.dropna()
        bins_edges = np.arange(0, 101, 10)
        hist, bins = np.histogram(ages, bins=bins_edges)

        # plotting
        plt.hist(ages, bins=bins)
        plt.xlabel('Age')
        plt.ylabel('Number of Participants')
        plt.title('Age Distribution of Participants')
        plt.show()

        return hist, bins

    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.

    Returns
    -------
    df : pd.DataFrame
      A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
      the (ordinal) index after a reset.
        """
        data = self.data
        cleaned_data = pd.DataFrame()
        for old_index, row in data.iterrows():
            email = row['email']
            if email.count('@') != 1:
                continue
            elif email.index('@') == 0 or email.index('@') == len(email) - 1:
                continue
            elif '.' not in email:
                continue
            elif email.index('.') == 0 or email.index('.') == len(email) - 1:
                continue
            elif '@.' in email:
                continue
            else:
                cleaned_data = pd.concat([cleaned_data, row.to_frame().T], ignore_index=True)

        return cleaned_data

    def fill_na_with_mean(self) -> Tuple[pd.DataFrame, np.ndarray]:
        """Finds, in the original DataFrame, the subjects that didn't answer
        all questions, and replaces that missing value with the mean of the
        other grades for that student.

    Returns
    -------
    df : pd.DataFrame
      The corrected DataFrame after insertion of the mean grade
    arr : np.ndarray
          Row indices of the students that their new grades were generated
        """
        data = self.data
        df = pd.DataFrame()
        arr = np.empty(0)

        for index, row in data.iterrows():
            grades = row[['q1', 'q2', 'q3', 'q4', 'q5']]
            pd.set_option("future.no_silent_downcasting", True)
            grades.replace('nan', np.nan, inplace=True)
            if grades.isnull().values.any():
                row_avg = np.nanmean(grades)
                grades.fillna(row_avg, inplace=True)
                arr = np.append(arr, index)
            row[['q1', 'q2', 'q3', 'q4', 'q5']] = grades
            row = row.to_frame().T
            df = pd.concat([df, row], ignore_index=True)

        return df, arr

    def score_subjects(self, maximal_nans_per_sub: int = 1) -> pd.DataFrame:
        """Calculates the average score of a subject and adds a new "score" column
        with it.

        If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
        score should be NA. Otherwise, the score is simply the mean of the other grades.
        The datatype of score is UInt8, and the floating point raw numbers should be
        rounded down.

        Parameters
        ----------
        maximal_nans_per_sub : int, optional
            Number of allowed NaNs per subject before giving a NA score.

        Returns
        -------
        pd.DataFrame
            A new DF with a new column - "score".
        """
        import math

        data = self.data
        scores = []
        df = data.copy()

        for index, row in data.iterrows():
            grades = row[['q1', 'q2', 'q3', 'q4', 'q5']]
            pd.set_option("future.no_silent_downcasting", True)
            grades.replace('nan', np.nan, inplace=True)
            row_avg = math.floor(np.nanmean(grades))
            if grades.isnull().values.sum() > maximal_nans_per_sub:
                scores.append(pd.NA)
            else:
                scores.append(row_avg)

        scores = pd.Series(scores).astype(pd.UInt8Dtype())
        df['score'] = scores

        return df









